int	ft_str_is_alpha(char *str)
{
	int	i;
	int	j;

	j = 0;
	i = 0;
	if (str[0] == '\0')
		return (1);
	else
	{
		while (*(str + i) != '\0')
		{
			if ((str[i] < 'a' && str[i] > 'Z') || str[i] < 'A' || str[i] > 'z')
			{
				j++;
			}
			i++;
		}
	}
	if (j != 0)
		return (0);
	else
		return (1);
}
